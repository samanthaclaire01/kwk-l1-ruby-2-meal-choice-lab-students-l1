def breakfast(cereal)
  puts "My favorite breakfast item is #{cereal}."
end
breakfast("Cheerios")
def lunch(sandwich, chips)
  puts "For lunch, I would like to eat #{sandwich} and #{chips} chips."
end
lunch("Grilled Cheese", "Lays")
def snack(fruit, cheese)
  puts "This afternoon I will eat #{fruit} and #{cheese}."
end
snack("Apples", "Monterey Jack cheese")
def dinner(appetizer, main_course, side)
  puts "Tonight, my mom is making #{appetizer}, #{main_course}, and #{side}."
end
dinner("Caesar salad", "steak", "rice")